-- 1) 동일한 직무를 수행하는 직원들의 직무아이디와 직원 수를 조회
SELECT JOB_ID, COUNT(*) CNT
FROM EMPLOYEES
GROUP BY JOB_ID;

-- 2) 동일한 월급을 받는 직원들의 월급과 직원 수를 조회
SELECT SALARY, COUNT(*) CNT
FROM EMPLOYEES
GROUP BY SALARY;

-- 3) 부서별로 부서아이디, 소속직원수, 월급합계를 조회
--    단, 부서ID가 NULL인 것은 제외
SELECT DEPARTMENT_ID, COUNT(DEPARTMENT_ID) CNT, SUM(SALARY) SUMSAL
FROM EMPLOYEES
WHERE DEPARTMENT_ID IS NOT NULL
GROUP BY DEPARTMENT_ID;

-- 4) 부서별로 부서아이디, 최대급여, 최소급여, 최대급여와 최소급여의 차를 조회
--    단, 부서아이디가 NULL인 것은 제외
SELECT DEPARTMENT_ID, MAX(SALARY) MAXSAL, MIN(SALARY) MINSAL, MAX(SALARY)-MIN(SALARY) DIFSAL
FROM EMPLOYEES
WHERE DEPARTMENT_ID IS NOT NULL
GROUP BY DEPARTMENT_ID;

-- 5) 부서별로 직원의 수가 5명 이상인 부서의 부서아이디와 직원 수를 조회
SELECT DEPARTMENT_ID, COUNT(*)
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID
HAVING COUNT(DEPARTMENT_ID)>=5;

-- 6) 커미션퍼센트가 NULL이 아니면서 동일한 값인 직원들의 커미션퍼센트와 직원 수를 조회
SELECT COMMISSION_PCT, COUNT(*) "직원수"
FROM EMPLOYEES
WHERE COMMISSION_PCT IS NOT NULL
GROUP BY COMMISSION_PCT;

-- 7) 6)에서 직원수가 3~5 범위에 있는 것들만 조회
SELECT COMMISSION_PCT, COUNT(*) "직원수"
FROM EMPLOYEES
WHERE COMMISSION_PCT IS NOT NULL
GROUP BY COMMISSION_PCT
HAVING COUNT(COMMISSION_PCT) BETWEEN 3 AND 5;

-- 8) NULL이 아니고 0.3을 넘는 커미션퍼센트인 직원들에 대해
--    커미션퍼센트별로 커미션퍼센트와 직원 수를 조회
SELECT COMMISSION_PCT, COUNT(*) "직원수"
FROM EMPLOYEES
WHERE COMMISSION_PCT IS NOT NULL
GROUP BY COMMISSION_PCT
HAVING COMMISSION_PCT>=0.3;

-- 9) MANAGER_ID가 존재하는 부서들 중에서 LOCATION_ID별로
--    LOCATION_ID와 부서의 수를 조회
SELECT LOCATION_ID, COUNT(*) "부서의 수"
FROM DEPARTMENTS
WHERE MANAGER_ID IS NOT NULL
GROUP BY LOCATION_ID;

-- 10) 부서별로 고용년도가 같은 직원들의 부서아이디와 고용년도, 직원 수를
--    부서아이디가 큰 순으로 정렬하여 조회
SELECT DEPARTMENT_ID, TO_CHAR(HIRE_DATE, 'YYYY') HIREYEAR, COUNT(*) CNT
FROM EMPLOYEES
WHERE DEPARTMENT_ID IS NOT NULL
-- 2차 그룹핑 : 부서로 그룹핑한 후 부서내에서 고용년도로 다시 그룹핑
GROUP BY DEPARTMENT_ID, TO_CHAR(HIRE_DATE, 'YYYY')
ORDER BY DEPARTMENT_ID DESC;

-- 11) MANAGER_ID가 존재하는 직원들에 대해 부서별, MANAGER_ID별
--     부서아이디, 매니저아이디, 직원 수를 조회
--     단, 부서아이디가 NULL인 부서는 제외
SELECT DEPARTMENT_ID, MANAGER_ID, COUNT(*) "직원 수"
FROM EMPLOYEES
WHERE MANAGER_ID IS NOT NULL
	  AND DEPARTMENT_ID IS NOT NULL
GROUP BY DEPARTMENT_ID, MANAGER_ID;

-- 12) 국가별, 위치별, 부서별, 직무별 직원의 수를 조회**********************************************
SELECT
	LOCATION_ID,
