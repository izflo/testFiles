-- 17) 부서명에 'IT'가 포함된 부서의 수와 그렇지 않은 부서의 수를 조회
SELECT COUNT(*) CNT
FROM DEPARTMENTS
WHERE INSTR(DEPARTMENT_NAME, 'IT')>0 -- 포함됐으면 인덱스 값이 나오니까 0보다 클 것임
UNION
SELECT COUNT(*) CNT
FROM DEPARTMENTS
WHERE INSTR(DEPARTMENT_NAME, 'IT')=0;

-- 18) 2006년 이전에 입사한 직원들의 월급합계와 2006년 이후에 입사한
--       직원들의 월급합계를 조회
SELECT SUM(SALARY) SALSUM
FROM EMPLOYEES
WHERE HIRE_DATE < TO_DATE('2006-01-01')
UNION
SELECT SUM(SALARY) SALSUM
FROM EMPLOYEES
WHERE HIRE_DATE >= TO_DATE('2007-01-01');

-- 19) 국가별, 지역별, 부서별 직원 수와 월급합계를 조회
SELECT COUNTRY_ID , LOCATION_ID , DEPARTMENT_ID , COUNT(*) CNT, SUM(SALARY) SUMSAL
FROM EMP_DETAILS_VIEW
GROUP BY COUNTRY_ID, LOCATION_ID, DEPARTMENT_ID;

-- 20) 직무명에 'Sales'가 들어가는 직원들의 부서별 월급합계를 조회
