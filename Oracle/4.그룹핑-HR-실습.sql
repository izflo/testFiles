-- 13) 연도별 입사직원의 수를 조회
SELECT TO_CHAR(HIRE_DATE, 'YYYY') "연도별", COUNT(*) "입사직원의 수"
FROM EMPLOYEES
GROUP BY TO_CHAR(HIRE_DATE, 'YYYY');

-- 14) 전화번호 중 동일한 지역번호를 사용하는 직원의 수를 조회
--       단, 지역번호는 앞 3자리로 함
SELECT SUBSTR(PHONE_NUMBER, 1, 3) "지역번호", COUNT(*) "명"
FROM EMPLOYEES
GROUP BY SUBSTR(PHONE_NUMBER, 1, 3)

-- 15) 부서별 연간총급여((월급+월급*커미션퍼센트)*12)를 산출하고
--       연간총급여가 높은 순으로 부서아이디와 연간총급여를 조회
--       단, 부서가 정해지지 않은 경우는 제외
--SELECT SALARY 
--FROM EMPLOYEES e, DEPARTMENTS d
--GROUP BY d.DEPARTMENT_ID;
SELECT DEPARTMENT_ID, SUM((SALARY+SALARY*NVL(COMMISSION_PCT,0))*12) TOTAL 
-- DEPARTMENT_ID가 그룹핑 되어있음***********************
-- 그럼 그 그룹마다 또 내용이 여러가지 있을거아님
-- 그럼 SUM/COUNT로 그룹별로 함수 실행
FROM EMPLOYEES
WHERE DEPARTMENT_ID IS NOT NULL
GROUP BY DEPARTMENT_ID 
ORDER BY TOTAL DESC;


-- 16) 월급이 10000이상인 직원의 수와 10000미만인 직원의 수를 조회
